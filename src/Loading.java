    @Override
    public void run(){
        try { 
            for(int i=0;i<=200;i++){  // 200 ms 
                s++;
                int m=jProgressBar1.getMaximum();
                int v=jProgressBar1.getValue();
                if(v<m){  // if the current value is less than max increment the progress value by 1
                    jProgressBar1.setValue(jProgressBar1.getValue()+1);
                }
                else{
                    i=201;   // when i is out of range relocate to my page
                    setVisible(false);
                    Dashboard ob = new Dashboard();  // instiatiate new object from Dashboard
                    ob.setVisible(true);
                }
                Thread.sleep(50);  //method can be used to pause the execution of current thread for specified time in milliseconds.
            }                      
        } catch(Exception e){
            
        }
    }

